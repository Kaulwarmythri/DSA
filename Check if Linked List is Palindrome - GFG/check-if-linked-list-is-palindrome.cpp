class Solution{
  public:
    bool isPalindrome(Node *head) {
        return check(head, head);
    }
    
    bool check(Node* &head, Node *end) {
        if(!end) return true;
        
        bool isPal = check(head, end->next);
        if(head->data != end->data) return false;
        head = head->next;
        
        return isPal;
    }
};



